def tabify(s):
  res = []
  for line in s.split("\n"):
    if not line:
      continue
    directive, number = line.split()
    number = int(number)
    if directive == ".zero":
      res = res + [0] * number
    elif directive == ".byte":
      res.append(number)
    else:
      assert False
  return res

