//	Saves machine to ğ‘¥ and activates ğ‘¦, i.e.
//
//	    getcontext(x);
//	    setcontext(y);
//
//	Except using this API is safer and goes 2x faster:
//
//	    swapcontext(x, y);
//
//	@return	0 on success, or -1 w/ errno
//	@returnstwice
	.ftrace1
swapcontext:
	.ftrace2
#include "libc/calls/getcontext.inc"
#ifdef __x86_64__
	push	%rbp
	mov	%rsp,%rbp
	push	%rsi
	push	%rsi
	call	__swapcontextsig
	pop	%rdi
	pop	%rdi
	pop	%rbp
	test	%eax,%eax
	jnz	1f
#elif defined(__aarch64__)
	stp	x29,x30,[sp,#-32]!
	mov	x29,sp
	str	x1,[sp,16]
	bl	__swapcontextsig
	ldr	x1,[sp,16]
	ldp	x29,x30,[sp],#32
	cbnz	w0,1f
	mov	x0,x1
#endif
	jmp	__tailcontext
1:	ret
	.endfn	swapcontext,globl
