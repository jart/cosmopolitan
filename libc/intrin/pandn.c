/**
 * Nands 128-bit integers.
 *
 * @param ğ‘ [w/o] receives result
 * @param ğ‘ [r/o] supplies first input vector
 * @param ğ‘ [r/o] supplies second input vector
 * @mayalias
 */
void(pandn)(uint64_t a[2], const uint64_t b[2], const uint64_t c[2]) {
  unsigned i;
  for (i = 0; i < 2; ++i) {
    a[i] = ~b[i] & c[i];
  }
}
