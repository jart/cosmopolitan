/**
 * Adds adjacent 32-bit integers.
 *
 * @param ğ‘ [w/o] receives reduced ğ‘ and ğ‘ concatenated
 * @param ğ‘ [r/o] supplies two pairs of ints
 * @param ğ‘ [r/o] supplies two pairs of ints
 * @note goes fast w/ ssse3 (intel c. 2004, amd c. 2011)
 * @mayalias
 */
void(phaddd)(int32_t a[4], const int32_t b[4], const int32_t c[4]) {
  int32_t t[4];
  t[0] = b[0] + b[1];
  t[1] = b[2] + b[3];
  t[2] = c[0] + c[1];
  t[3] = c[2] + c[3];
  memcpy(a, t, sizeof(t));
}
