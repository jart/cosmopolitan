/**
 * Multiplies 32-bit signed integers.
 *
 * @param ğ‘ [w/o] receives result
 * @param ğ‘ [r/o] supplies first input vector
 * @param ğ‘ [r/o] supplies second input vector
 * @see pmuludq()
 * @mayalias
 */
void(pmulld)(int32_t a[4], const int32_t b[4], const int32_t c[4]) {
  unsigned i;
  int32_t r[4];
  for (i = 0; i < 4; ++i) {
    r[i] = b[i] * c[i];
  }
  memcpy(a, r, 16);
}
