#include "libc/intrin/shufpd.h"

/**
 * Shuffles double vector.
 * @param ð‘š needs to be a literal, constexpr, or embedding
 * @mayalias
 */
void(shufpd)(double b[2], const double a[2], uint8_t m) {
  double t[2];
  t[0] = a[(m & 0b0000001) >> 0];
  t[1] = a[(m & 0b0000010) >> 1];
  b[0] = t[0];
  b[1] = t[1];
}
