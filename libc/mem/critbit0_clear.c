static void critbit0_clear_traverse(void *top) {
  unsigned char *p = top;
  if (1 & (intptr_t)p) {
    struct CritbitNode *q = (void *)(p - 1);
    critbit0_clear_traverse(q->child[0]);
    critbit0_clear_traverse(q->child[1]);
    free(q);
  } else {
    free(p);
  }
}

