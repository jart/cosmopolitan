/	Copies memory.
/
/	DEST and SRC must not overlap, unless DEST‚â§SRC.
/
/	@param	rdi is dest
/	@param	rsi is src
/	@param	rdx is number of bytes
/	@return	original rdi copied to rax
/	@mode	long
/	@asyncsignalsafe
	.align	16
	.source	__FILE__
memcpy:	mov	%rdi,%rax
/	ùë†ùëôùëñùëëùëí
	.endfn	memcpy,globl

/	Copies memory w/ minimal impact ABI.
/
/	@param	rdi is dest
/	@param	rsi is src
/	@param	rdx is number of bytes
/	@clob	flags,rcx,xmm3,xmm4
/	@mode	long
	.align	16
MemCpy:	.leafprologue
	mov	$.Lmemcpytab.ro.size,%ecx
	cmp	%rcx,%rdx
	cmovb	%rdx,%rcx
	jmp	*memcpytab(,%rcx,8)
.Lanchorpoint:
.L32r:	cmp	$1024,%rdx
	jae	.Lerms
.L32:	vmovdqu	-32(%rsi,%rdx),%ymm4
	mov	$32,%rcx
0:	add	$32,%rcx
	vmovdqu	-64(%rsi,%rcx),%ymm3
	vmovdqu	%ymm3,-64(%rdi,%rcx)
	cmp	%rcx,%rdx
	ja	0b
	vmovdqu	%ymm4,-32(%rdi,%rdx)
	vpxor	%ymm4,%ymm4,%ymm4
	vpxor	%ymm3,%ymm3,%ymm3
	jmp	.L0
.L16r:	cmp	$1024,%rdx
	jae	.Lerms
.L16:	movdqu	-16(%rsi,%rdx),%xmm4
	mov	$16,%rcx
0:	add	$16,%rcx
	movdqu	-32(%rsi,%rcx),%xmm3
	movdqu	%xmm3,-32(%rdi,%rcx)
	cmp	%rcx,%rdx
	ja	0b
	movdqu	%xmm4,-16(%rdi,%rdx)
	pxor	%xmm4,%xmm4
	pxor	%xmm3,%xmm3
	jmp	.L0
.L8:	push	%rbx
	mov	(%rsi),%rcx
	mov	-8(%rsi,%rdx),%rbx
	mov	%rcx,(%rdi)
	mov	%rbx,-8(%rdi,%rdx)
1:	pop	%rbx
.L0:	.leafepilogue
.L4:	push	%rbx
	mov	(%rsi),%ecx
	mov	-4(%rsi,%rdx),%ebx
	mov	%ecx,(%rdi)
	mov	%ebx,-4(%rdi,%rdx)
	jmp	1b
.L3:	push	%rbx
	mov	(%rsi),%cx
	mov	-2(%rsi,%rdx),%bx
	mov	%cx,(%rdi)
	mov	%bx,-2(%rdi,%rdx)
	jmp	1b
.L2:	mov	(%rsi),%cx
	mov	%cx,(%rdi)
	jmp	.L0
.L1:	mov	(%rsi),%cl
	mov	%cl,(%rdi)
	jmp	.L0
.Lerms:	cmp	kHalfCache3(%rip),%rdx
	ja	.Lnts
	push	%rdi
	push	%rsi
	mov	%rdx,%rcx
	rep movsb
	pop	%rsi
	pop	%rdi
	jmp	.L0
.Lnts:	movdqu	(%rsi),%xmm3
	movdqu	%xmm3,(%rdi)
	lea	16(%rdi),%rcx
	and	$-16,%rcx
	sub	%rdi,%rcx
	add	%rcx,%rdi
	add	%rcx,%rsi
	sub	%rcx,%rdx
	mov	$16,%rcx
0:	add	$16,%rcx
	movdqu	-32(%rsi,%rcx),%xmm3
	movntdq	%xmm3,-32(%rdi,%rcx)
	cmp	%rcx,%rdx
	ja	0b
	sfence
	movdqu	-16(%rsi,%rdx),%xmm3
	movdqu	%xmm3,-16(%rdi,%rdx)
	pxor	%xmm3,%xmm3
	jmp	.L0
	.endfn	MemCpy,globl,hidden

	.initro 300,_init_memcpy
memcpytab.ro:
	.byte	.L0-.Lanchorpoint
	.byte	.L1-.Lanchorpoint
	.byte	.L2-.Lanchorpoint
	.byte	.L3-.Lanchorpoint
	.rept	4
	.byte	.L4-.Lanchorpoint
	.endr
	.rept	8
	.byte	.L8-.Lanchorpoint
	.endr
	.rept	16
	.byte	.L16-.Lanchorpoint
	.endr
	.equ	.Lmemcpytab.ro.size,.-memcpytab.ro
	.endobj	memcpytab.ro
	.if	.Lmemcpytab.ro.size % 8
	.error	"moar jmptab"
	.endif
	.byte	.L16-.Lanchorpoint		# SSE2
	.byte	.L16r-.Lanchorpoint		# SSE2 + ERMS
	.byte	.L32-.Lanchorpoint		# AVX
	.byte	.L32r-.Lanchorpoint		# AVX + ERMS
	.byte	0,0,0,0
	.previous

	.initbss 300,_init_memcpy
memcpytab:
	.rept	.Lmemcpytab.ro.size
	.quad	0
	.endr
	.quad	0
	.endobj	memcpytab
	.previous

	.init.start 300,_init_memcpy
	pushpop	.Lmemcpytab.ro.size,%rcx
	ezlea	.Lanchorpoint,dx
	testb	X86_HAVE(AVX)+kCpuids(%rip)
	call	memjmpinit
	.init.end 300,_init_memcpy

