/	Copies memory.
/
/	DEST and SRC may overlap.
/
/	@param	rdi is dest
/	@param	rsi is src
/	@param	rdx is number of bytes
/	@return	original rdi copied to rax
/	@clob	flags,rcx
/	@asyncsignalsafe
memmove:
	mov	%rdi,%rax
/	ğ‘ ğ‘™ğ‘–ğ‘‘ğ‘’
	.endfn	MemMove,globl,hidden

MemMove:
	.leafprologue
	.profilable
	push	%rdi
	push	%rsi
	mov	%rdx,%rcx
	cmp	%rsi,%rdi
	jb	1f
	lea	-1(%rdi,%rcx),%rdi
	lea	-1(%rsi,%rcx),%rsi
	std
1:	rep movsb
	cld
	pop	%rsi
	pop	%rdi
	.leafepilogue
	.endfn	memmove,globl
	.source	__FILE__
