#include "libc/macros.h"
.yoink	__FILE__

/	Returns 10^x.
/
/	@param	ğ‘¥ is an 80-bit long double passed on stack in 16-bytes
/	@return	result of exponentiation on FPU stack in %st
tinymath_exp10l:
	push	%rbp
	mov	%rsp,%rbp
	.profilable
	fldt	16(%rbp)
	fldl2t
	fmulp	%st,%st(1)
	fld	%st
	frndint
	fsubr	%st,%st(1)
	fxch	%st(1)
	f2xm1
	fld1
	faddp
	fscale
	fstp	%st(1)
	pop	%rbp
	ret
	.endfn	tinymath_exp10l,globl
	.alias	tinymath_exp10l,exp10l
	.alias	tinymath_exp10l,pow10l
