#include "libc/macros.h"
.source	__FILE__

/	Returns 2^ğ‘¥.
/
/	@param	ğ‘¥ is an 80-bit long double passed on stack in 16-bytes
/	@return	result of exponentiation on FPU stack in %st
tinymath_exp2l:
	push	%rbp
	mov	%rsp,%rbp
	.profilable
	fldt	16(%rbp)
	fld	%st
	frndint
	fsubr	%st,%st(1)
	fxch
	f2xm1
	fadds	.Lone(%rip)
	fscale
	fstp	%st(1)
	pop	%rbp
	ret
	.endfn	tinymath_exp2l,globl
	.alias	tinymath_exp2l,exp2l

	.rodata.cst4
.Lone:	.float	1.0
