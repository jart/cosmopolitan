/	Rounds to nearest integer, away from zero.
/
/	@param	ğ‘¥ is double scalar in low half of %xmm0
/	@return	64-bit signed integer in %rax
/	@see	round(), lrint()
tinymath_lround:
	.leafprologue
	.profilable
	movsd	A(%rip),%xmm2
	movsd	B(%rip),%xmm1
	andpd	%xmm0,%xmm2
	orpd	%xmm2,%xmm1
	addsd	%xmm0,%xmm1
	cvttsd2siq %xmm1,%rax
	.leafepilogue
	.endfn	tinymath_lround,globl
	.alias	tinymath_lround,lround
	.alias	tinymath_lround,llround

	.rodata.cst16
A:	.quad	0x8000000000000000,0
B:	.quad	0x3fdfffffffffffff,0
