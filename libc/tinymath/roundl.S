/	Rounds to nearest integer, away from zero.
/
/	@param	ð‘¥ is an 80-bit long double passed on stack in 16-bytes
/	@return	result of exponentiation on FPU stack in %st
tinymath_roundl:
	push	%rbp
	mov	%rsp,%rbp
	.profilable
	push	%rax
	fldt	16(%rbp)
	fnstcw	-8(%rbp)
	movzwl	-8(%rbp),%edx
	and	$0b11110011,%dh			# RC (Rounding Control)
	or	$0b00000100,%dh			# â†’-âˆž a.k.a. floor()
	mov	%dx,-4(%rbp)
	fxam					# C1 is set to sign bit
	fnstsw
	fabs
	test	$FPU_C1>>8,%ah
	fadds	.Lhalf(%rip)
	fldcw	-4(%rbp)
	frndint
	fldcw	-8(%rbp)
	je	1f
	fchs
1:	leave
	ret
	.endfn	tinymath_roundl,globl
	.alias	tinymath_roundl,roundl
	.source	__FILE__

	.rodata.cst4
.Lhalf:	.float	.5
