double scalb(double x, double y)
{
	if (isunordered(x, y))
		return x*y;
	if (!isfinite(y)) {
		if (y > 0.0)
			return x*y;
		else
			return x/(-y);
	}
	if (rint(y) != y) return (y-y)/(y-y);
	if ( y > 65000.0) return scalbn(x, 65000);
	if (-y > 65000.0) return scalbn(x,-65000);
	return scalbn(x,(int)y);
}
